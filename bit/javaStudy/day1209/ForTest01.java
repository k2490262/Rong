class ForTest01 {
	public static void main(String[] args) {
		System.out.println("구구단 2단 출력");
		System.out.println("2");
		System.out.println("2 * 1 = 2");
		System.out.println(2 + " * " + 1 + " = " + (2 * 1));
		System.out.println(2 + " * " + 2 + " = " + (2 * 2));
		System.out.println(2 + " * " + 3 + " = " + (2 * 3));
	}
}
