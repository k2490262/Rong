class Ex02 {
	public static void main(String[] args) {
		int x = 3;
		int y = 5;
		int z = 2;

		if (x == z + 1) {
			System.out.println(true);
		}

		if (x != y - z) {
			System.out.println(true);
		}

		if (x >= 3 && y <= 5) {
			System.out.println(true);
		}

		if (x > 5 || z < 3) {
			System.out.println(true);
		}

		if (x + 1 == y - 1) {
			System.out.println(true);
		}
	}
}
